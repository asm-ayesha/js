// Use a for...of loop to concatenate all the elements of an array into a single string.

let numbers = ['Tom', 'Tim', 'Tin', 'Tik'];
console.log(numbers)

let result ='';
for(let number of numbers){
    result +=number;
}
console.log(result)

                                                                                                                                                                                                                                                                                                                                                                                